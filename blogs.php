						<h4> DTI Crafts Road Map for AI, Fourth Industrial Revolution </h4>
						<p><i> November 12, 2019 </i></p>
					</center>
				</div>

				<div class = "article-image">
					<center>
						<img src = "images/blogs/dti-crafts-roadmaps.png" alt = "DTI Crafts Road Map">

