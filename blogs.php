					<center>
						<h4> Christmas the Filipino Way </h4>
						<p><i> December 27, 2019 </i></p>
					</center>
				</div>

				<div class = "article-image">
					<center>
						<img src = "images/blogs/christmas-the-filipino-way.jpg" alt = "Christmas the Filipino Way">

						<p class = "image-description"><i> Everyday from Dec. 16 to 24 annually, Filipino Catholics troop to churches at the break of dawn to attend the Simbang Gabi. — Ervin Malicdem </i></p>
					</center>
