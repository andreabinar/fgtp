					</p>

					<p>
						<i>“Hopefully, maubusan at mawalan na ng isusumbong kasi wala nang (I hope complaints will fall since there will be no more) human intervention. Every signature, every stop that you need to talk to a government employee is always an opportunity for corruption kaya itong streamlining natin na ginagawa nito is end-to-end (We are hoping to achieve end-to-end streamlining of the process),”</i> Anti-Red Tape Authority (ARTA) Director-General Jeremiah B. Belgica told reporters.
